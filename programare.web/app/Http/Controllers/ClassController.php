     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'class_name' => 'required|min:2|max:255'
        ]);

        $class = new StudentsClass;

        $class->name = $request->class_name;
        $class->save();

        return back();
